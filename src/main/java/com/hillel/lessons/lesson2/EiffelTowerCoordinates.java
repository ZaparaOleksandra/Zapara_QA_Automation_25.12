public class EiffelTowerCoordinates {
    public static void main(String[] args) {

        double north = 48.8584;
        double east = 2.2945;
        char degree = '\u00B0';
        System.out.println("Coordinates of Eiffel Tower are:");
        System.out.println("North: " + north + degree + "N");
        System.out.println("East: " + east + degree + "E");

    }
}
